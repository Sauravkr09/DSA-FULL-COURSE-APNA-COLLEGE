public class DataType {
    public static void main(String[] args) {
        byte b = 8; // Stores a number between -128 to 127
        System.out.println(b);
        
        char ch = 'a'; // Stores a single Unicode character
        System.out.println(ch);
        
        @SuppressWarnings("unused")
        boolean var = true; // Stores a boolean value (true or false)
        
        double price = 10.5; // Stores a decimal number with double precision
        System.out.println(price);
        
        @SuppressWarnings("unused")
        int number = 25; // Stores a whole number (integer)
        
        @SuppressWarnings("unused")
        short n = 240; // Stores a small whole number
        
        // long - Stores a larger whole number (long integer)
        // double - Stores a decimal number with double precision
    }
}
